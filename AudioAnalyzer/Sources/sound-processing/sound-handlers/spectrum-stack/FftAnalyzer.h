#pragma once
#include "../SoundHandler.h"
#include "RainmeterWrappers.h"
#include "../../../audio-utils/FFT.h"
#include "../../../audio-utils/FftCascade.h"

namespace rxtd::audio_analyzer {
	class FftAnalyzer : public SoundHandler {
	public:
		enum class SizeBy {
			BIN_WIDTH,
			SIZE,
			SIZE_EXACT
		};

		struct Params {
		private:
			friend FftAnalyzer;

			SizeBy legacy_sizeBy;

			double legacy_attackTime;
			double legacy_decayTime;

			double binWidth;
			double overlap;

			index cascadesCount;

			double randomTest;
			double randomDuration;
			bool correctZero;
			bool legacyAmplification;

			// autogenerated
			friend bool operator==(const Params& lhs, const Params& rhs) {
				return lhs.legacy_sizeBy == rhs.legacy_sizeBy
					&& lhs.legacy_attackTime == rhs.legacy_attackTime
					&& lhs.legacy_decayTime == rhs.legacy_decayTime
					&& lhs.binWidth == rhs.binWidth
					&& lhs.overlap == rhs.overlap
					&& lhs.cascadesCount == rhs.cascadesCount
					&& lhs.randomTest == rhs.randomTest
					&& lhs.randomDuration == rhs.randomDuration
					&& lhs.legacyAmplification == rhs.legacyAmplification
					&& lhs.correctZero == rhs.correctZero;
			}

			friend bool operator!=(const Params& lhs, const Params& rhs) {
				return !(lhs == rhs);
			}
		};

	private:
		Params params{ };

		index samplesPerSec{ };

		index fftSize = 0;
		index inputStride = 0;

		index randomBlockSize = 0;
		index randomCurrentOffset = 0;

		enum class RandomState { ON, OFF } randomState{ RandomState::ON };

		std::vector<audio_utils::FftCascade> cascades{ };

		audio_utils::FFT fft{ };

	public:
		FftAnalyzer() = default;
		~FftAnalyzer() = default;

		/** This class is non copyable */
		FftAnalyzer(const FftAnalyzer& other) = delete;
		FftAnalyzer(FftAnalyzer&& other) = delete;
		FftAnalyzer& operator=(const FftAnalyzer& other) = delete;
		FftAnalyzer& operator=(FftAnalyzer&& other) = delete;

		static std::optional<Params> parseParams(const OptionMap& optionMap, Logger& cl);

		double getFftFreq(index fft) const;

		index getFftSize() const;

		void setSamplesPerSec(index samplesPerSec) override;
		void reset() override;

		void _process(const DataSupplier& dataSupplier) override;

		void _finish(const DataSupplier& dataSupplier) override {
		}

		index getLayersCount() const override;
		array_view<float> getData(index layer) const override;

		bool getProp(const isview& prop, utils::BufferPrinter& printer) const override;

		void setParams(Params params, Channel channel);

	private:
		void processRandom(index waveSize);

		void updateParams();
	};
}
