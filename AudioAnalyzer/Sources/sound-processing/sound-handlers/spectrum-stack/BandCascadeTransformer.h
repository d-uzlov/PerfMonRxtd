#pragma once
#include "../SoundHandler.h"
#include "BandResampler.h"

namespace rxtd::audio_analyzer {
	class BandCascadeTransformer : public SoundHandler {
	public:
		enum class MixFunction {
			AVERAGE,
			PRODUCT,
		};

		struct Params {
			double minWeight{ };
			double targetWeight{ };
			double weightFallback{ };

			double zeroLevel{ };
			double zeroLevelHard{ };
			double zeroWeight{ };

			MixFunction mixFunction{ };

			// autogenerated
			friend bool operator==(const Params& lhs, const Params& rhs) {
				return lhs.minWeight == rhs.minWeight
					&& lhs.targetWeight == rhs.targetWeight
					&& lhs.weightFallback == rhs.weightFallback
					&& lhs.zeroLevel == rhs.zeroLevel
					&& lhs.zeroLevelHard == rhs.zeroLevelHard
					&& lhs.zeroWeight == rhs.zeroWeight
					&& lhs.mixFunction == rhs.mixFunction;
			}

			friend bool operator!=(const Params& lhs, const Params& rhs) {
				return !(lhs == rhs);
			}
		};

	private:
		Params params{ };

		BandResampler* resamplerPtr = nullptr;

		struct AnalysisInfo {
			string analysisString{ };
			index minCascadeUsed = -1;
			index maxCascadeUsed = -1;
			std::vector<index> bandEndCascades{ };
			bool anyCascadeUsed{ };
		} analysis;

		struct CascadeMeta {
			index offset{ };
			index nextChunkIndex{ };
			array_view<float> data;
		};

		std::vector<CascadeMeta> snapshot;

	public:
		[[nodiscard]]
		bool checkSameParams(const std::any& p) const override {
			return compareParamsEquals(params, p);
		}

		void setParams(const std::any& p) override {
			params = std::any_cast<Params>(p);
		}

		[[nodiscard]]
		ParseResult parseParams(
			const OptionMap& om, Logger& cl, const Rainmeter& rain,
			index legacyNumber
		) const override;

	protected:
		[[nodiscard]]
		ConfigurationResult vConfigure(Logger& cl) override;

	public:
		void vProcess(array_view<float> wave, clock::time_point killTime) override;

		[[nodiscard]]
		index getStartingLayer() const override {
			return analysis.minCascadeUsed;
		}

		bool vGetProp(const isview& prop, utils::BufferPrinter& printer) const override;

	private:
		[[nodiscard]]
		float computeForBand(index band) const;

		[[nodiscard]]
		static AnalysisInfo computeAnalysis(BandResampler& resampler, double minWeight, double targetWeight);
	};
}
