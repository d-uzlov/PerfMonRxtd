#pragma once
#include "SoundHandler.h"
#include "RainmeterWrappers.h"
#include "../../audio-utils/LogarithmicIRF.h"

namespace rxtd::audio_analyzer {
	class BlockHandler : public SoundHandler {
	public:
		struct Params {
		private:
			friend BlockHandler;

			double attackTime { };
			double decayTime { };
			double resolution { };
			bool subtractMean { };

			// autogenerated
			friend bool operator==(const Params& lhs, const Params& rhs) {
				return lhs.attackTime == rhs.attackTime
					&& lhs.decayTime == rhs.decayTime
					&& lhs.resolution == rhs.resolution
					&& lhs.subtractMean == rhs.subtractMean;
			}

			friend bool operator!=(const Params& lhs, const Params& rhs) {
				return !(lhs == rhs);
			}
		};

	private:
		Params params { };

		index samplesPerSec { };

		audio_utils::LogarithmicIRF filter { };
		index blockSize { };

	protected:
		index counter = 0;

	private:
		float result = 0.0;

		mutable string propString { };

	public:
		void setParams(Params params);

		void setSamplesPerSec(index samplesPerSec) final;
		void reset() final;

		void process(const DataSupplier& dataSupplier) final;
		void processSilence(const DataSupplier& dataSupplier) final;
		void finish(const DataSupplier& dataSupplier) final;

		array_view<float> getData(layer_t layer) const final {
			return { &result, 1 };
		}

		const wchar_t* getProp(const isview& prop) const override;

		static std::optional<Params> parseParams(const utils::OptionMap& optionMap, utils::Rainmeter::Logger &cl);

	protected:
		void setNextValue(double value);

		index getBlockSize() const {
			return blockSize;
		}
	private:
		void recalculateConstants();
		virtual void _process(array_view<float> wave, float average) = 0;
		virtual void finishBlock() = 0;
		virtual void _reset() = 0;
		virtual void _setSamplesPerSec(index samplesPerSec) { }
		virtual bool isAverageNeeded() const {
			return false;
		}
	};

	class BlockRms : public BlockHandler {
		double intermediateResult = 0.0;

	public:
		void _process(array_view<float> wave, float average) override;

	protected:
		bool isAverageNeeded() const override {
			return true;
		}
		void finishBlock() override;
		void _reset() override;
	};

	class BlockPeak : public BlockHandler {
		double intermediateResult = 0.0;

	public:
		void _process(array_view<float> wave, float average) override;

	protected:
		void finishBlock() override;
		void _reset() override;
	};
}
